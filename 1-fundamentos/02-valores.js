// ===== Tipos de datos primitivos =====
// Existen diferentes tipos de datos que podemos utilizar en un programa JavaScript. Por ejemplo,
const x = 5;
const y = "Hello";
//Aquí, 5 es un dato entero. "Hola" es un dato de cadena.



// ==> Numeros
// Para asignar un número, basta con copiarlo:
let number = 40;
const pi = 3.14;



// ==> Strings:
// Es un tipo de dato utilizado para representar textos. 
// En JavaScript existen tres maneras de tipar cadenas de texto:
const nombre = 'ram';
const otroNombre = "hari";
const resultado = `Sus nombres son ${nombre} y ${otroNombre}`;



// ==> Booleanos:
// Este tipo de datos representa entidades lógicas. 
// Se utilizan para validar condiciones binarias, condiciones de verdadero y falso.
const datosComprobados = true;
const valorContado = false;



// ==> Valores vacios:
// Son valores reservados cuando quede algun valor faltante en memoria. Suele ser usando
// cuando el desarrollador desea esperar la asignación del valor.

// Espacio en memoria vacío.
let ganasDeVivir = null;

// Variable sin especificar.
let objetoVolador = undefined;
// "undefined" es el valor por defecto al declarar una variable.



// ==> Arreglos:
// Un arreglo es una colección ordenada de datos. Son usados para almacenar múltiples valores
// en una sola variable.

// Su sintaxis consta de separar los valores por "," y
// encerrarlos entre "[]"
let numeritos = [1, 2, 3, 4];



// ==> Objeto:
// JavaScript está diseñado en un paradigma simple basado en objetos. Un objeto es un tipo de dato
// complejo que nos permite almacenar colecciones de datos. Por ejemplo,
const miCarro = {
	marca: "Ford",
	modelo: "Mustang",
	anio: 1969,
}

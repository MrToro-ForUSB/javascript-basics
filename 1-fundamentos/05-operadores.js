// Operadores
// Un operador es un signo o símbolo que especifica el tipo de cálculo que se realiza en una
// expresión.

3 + 2;
50 - 10;
10 * 3;
20 / 2;
"Diego " + "De Granda";

var a = 1;
// El operador = es el operador de asignación.

// Operadores lógicos:
// Los operadores lógicos son los operadores que se utilizan para realizar cálculos lógicos.

!false;
// Contradice el valor de un valor booleano.

// == igual a
3 == "3";
// Compara que los valores de los contenidos sean iguales, sin importar el tipo de dato.

// === igual y del mismo tipo
3 === "3";
// Compara que los valores de los contenidos sean iguales y del mismo tipo.

// menor, menor o igual, mayo, mayor o igual
5 < 3, 5 <= 3, 5 > 3, 5 >= 3;

a && b;
// Valida que ambos valores sean verdaderos.

true || false;
// Valida que al menos uno de los valores sea verdadero.

// Operaciones de suma y resta:

edad++;
// Incrementa el valor de la variable en 1.
edad += 2;
// Incrementa el valor de la variable la cantidad especificada.

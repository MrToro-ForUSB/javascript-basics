// Valores Truthy y Falsy

// Truthy es un valor que representa un valor verdadero.
// Falsy es un valor que representa un valor falso.

// Valores Truthy (true)
"hola";
30;
{ a: 1; }
[1, 3];
true;
function a() {}

// Valores Falsy (False)
""; // un string vacío
0 - 0;
null;
NaN;
false;
undefined;

// Estos valores son usados para determinar si una condición es verdadera o falsa.
if (InputEvent.value) {
	// ...
}

// Tipos de datos primitivos:
// Se llama tipo primitivo a los tipos de datos originales de un lenguaje de programación, esto
// es, aquellos que nos proporciona el lenguaje y con los que podemos construir tipos de datos
// abstractos y estructuras de datos.

// Numeros:
// Para asignar un número, basta con copiarlo:
40
3.14

// Strings:
// Es un tipo de dato utilizado para representar textos.

// En JavaScript existen tres maneras de tipar cadenas de texto:

// Con comillas dobles:
("Hernesto Perez")

// Ésta es la forma más común utilizada en los lenguajes de programación.

// Con comillas simples:
"Hernesto Perez"`Hernesto Perez`
// Es un formato de cadenas interpoladas, apropiada para impimir texto
// combiado con variables.

// Booleanos:
// Se utilizan para validar condiciones binarias, condiciones de verdadero y falso.

// Condicion validada/verdadera.
true

// Condicion negada/falsa.
false

// Valores vacios:
// Son valores reservados cuando quede algun valor faltante en memoria. Suele ser usando
// cuando el desarrollador desea esperar la asignación del valor.

// Espacio en memoria vacío.
null

// Variable sin especificar:
undefined
// "undefined" es el valor por defecto al declarar una variable.

// Arreglos:
// Un arreglo es una colección ordenada de datos. Son usados para almacenar múltiples valores
// en una sola variable.

// Su sintaxis consta de separar los valores por "," y
// encerrarlos entre "[]"
[1, 2, 3, 4]

// Objeto:
// JavaScript está diseñado en un paradigma simple basado en objetos. Un objeto es una
// colección de propiedades, y una propiedad es una asociación entre un nombre (o clave) y un
// valor.

miCarro = {
	marca: "Ford",
	modelo: "Mustang",
	anio: 1969,
}
